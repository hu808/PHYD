package com.example.app01;

public class SearchS1 {
    private int 安全分數;
    private String 月份;

    public SearchS1() {
    }

    public SearchS1(int 安全分數, String 月份) {
        this.安全分數 = 安全分數;
        this.月份 = 月份;
    }

    public int get安全分數() {
        return 安全分數;
    }

    public void set安全分數(int 安全分數) {
        this.安全分數 = 安全分數;
    }

    public String get月份() {
        return 月份;
    }

    public void set月份(String 月份) {
        this.月份 = 月份;
    }
}
