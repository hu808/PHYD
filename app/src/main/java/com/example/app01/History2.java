package com.example.app01;

public class History2 {

    private String 危險項目;
    private String 時間點;

    public History2() {
    }

    public History2(String 危險項目, String 時間點) {
        this.危險項目 = 危險項目;
        this.時間點 = 時間點;
    }

    public String get危險項目() {
        return 危險項目;
    }
    public void set危險項目(String 危險項目) {
        this.危險項目 = 危險項目;
    }
    public String get時間點() {
        return 時間點;
    }
    public void set時間點(String 時間點) {
        this.時間點 = 時間點;
    }
}
