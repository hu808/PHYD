package com.example.app01;

public class Stringtocatch {

    private String 因子;
    private String 門檻值;

    public Stringtocatch() {
    }

    public Stringtocatch(String 因子, String 門檻值) {
        this.因子 = 因子;
        this.門檻值 = 門檻值;
    }

    public String get因子() {
        return 因子;
    }

    public void set因子(String 因子) {
        this.因子 = 因子;
    }

    public String get門檻值() {
        return 門檻值;
    }

    public void set門檻值(String 門檻值) {
        this.門檻值 = 門檻值;
    }
}
